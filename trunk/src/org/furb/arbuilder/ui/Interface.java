	public final void executarFuncaoControle(int funcao) {

		switch (funcao) {
		case 0:
			try {
				this.controle.getDAO().conectarBD();
				JOptionPane.showMessageDialog(this,
						"Banco de dados conectado com sucesso!", "Sucesso!",
						JOptionPane.INFORMATION_MESSAGE);
				this.btConectar.setEnabled(false);
				List<String> listaTabelasTemp = this.controle.getDAO()
						.getTabelasBD();
				((DefaultListModel) this.listaTabelas.getModel())
						.removeAllElements();
				for (int i = 0; i < listaTabelasTemp.size(); i++) {
					((DefaultListModel) this.listaTabelas.getModel())
							.addElement(listaTabelasTemp.get(i));
				}
			} catch (SQLException ex) {
				JOptionPane
						.showMessageDialog(
								this,
								"Erro ao tentar conectar-se do banco de dados.\nVerificar suas configuraÁıes de conex„o",
								"Erro!", JOptionPane.ERROR_MESSAGE);
			}
			break;

		case 1:
			if (DBUtil.getInstance().isCanConnect()) {
				if (this.controle.getDAO().desconectarBD()) {
					JOptionPane.showMessageDialog(this,
							"Banco de dados desconectado com sucesso!",
							"Sucesso!", JOptionPane.INFORMATION_MESSAGE);
					this.btConectar.setEnabled(true);
					((DefaultListModel) this.listaTabelas.getModel())
							.removeAllElements();
				} else {
					JOptionPane.showMessageDialog(this,
							"Erro ao tentar desconectar-se do banco de dados!",
							"Erro!", JOptionPane.ERROR_MESSAGE);
				}
				;
			} else {
				JOptionPane.showMessageDialog(this,
						"Banco de dados j· encontra-se desconectado !", "",
						JOptionPane.INFORMATION_MESSAGE);
			}
			break;
		}
	}

