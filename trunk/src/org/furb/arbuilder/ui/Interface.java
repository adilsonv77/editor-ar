	private void btExecutarSQLMousePressed(java.awt.event.MouseEvent evt) {
		if (!DBUtil.getInstance().isCanConnect()) {
			JOptionPane.showMessageDialog(this,
					"Nao e possivel executar esta expressao SQL!", "Erro!",
					JOptionPane.ERROR_MESSAGE);
			return;

		} else {

			try {
				if (sqlResultante.getText().trim().isEmpty()
						|| sqlResultante.getText().contains(
								"Erro, estrutura mal-formada neste operador!")) {
					JOptionPane.showMessageDialog(this,
							"Nao e possivel executar esta expressao SQL!",
							"Erro!", JOptionPane.ERROR_MESSAGE);
				} else {
					this.controle.getDAO().executarConsultaBD(
							this.sqlResultante.getText());
				}

			} catch (Exception ex) {
				JOptionPane.showMessageDialog(this,
						"Erro durante a execucao da expressao SQL!\n\n" + ex,
						"Erro!", JOptionPane.ERROR_MESSAGE);
				return;
			}

		}
	}

	private void btSalvarMousePressed(java.awt.event.MouseEvent evt) {
		try {
			// Salva as informaÁıes de conexao em XML
			xml.setCabecalhoConfiguracao();
			xml.setModeloGrafico(this.painelGrafico);
		} catch (ParserConfigurationException ex) {
			JOptionPane.showMessageDialog(this,
					"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
					JOptionPane.ERROR_MESSAGE);
			return;
		} catch (Exception ex) {
			JOptionPane.showMessageDialog(this,
					"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
					JOptionPane.ERROR_MESSAGE);
			return;
		}

		JFileChooser fileChooser = new JFileChooser();

		fileChooser.setDialogTitle("Salvar arquivo XML...");
		fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivos XML",
				"xml"));
		fileChooser.setVisible(true);

		int ret = fileChooser.showSaveDialog(this);

		if (ret == JFileChooser.APPROVE_OPTION) {
			try {
				xml.salvarXML(fileChooser.getSelectedFile().getAbsolutePath());
			} catch (ParserConfigurationException ex) {
				JOptionPane.showMessageDialog(this,
						"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
						JOptionPane.ERROR_MESSAGE);
				return;
			} catch (TransformerConfigurationException ex) {
				JOptionPane.showMessageDialog(this,
						"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
						JOptionPane.ERROR_MESSAGE);
				return;
			} catch (TransformerException ex) {
				JOptionPane.showMessageDialog(this,
						"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
						JOptionPane.ERROR_MESSAGE);
				return;
			} catch (FileNotFoundException ex) {
				JOptionPane.showMessageDialog(this,
						"Erro ao tentar salvar projeto!\n\n" + ex, "Erro!",
						JOptionPane.ERROR_MESSAGE);
				return;
			}
		}
	}

