	private void btAbrirMousePressed(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_btAbrirMousePressed

		if (arquivoAberto) {
			int ret = verificaDesejaSalvar();

			if (ret == 0) {
				btSalvarMousePressed(evt);
			}
			if (ret == 2) {
				return;
			}
		}

		JFileChooser fileChooser = new JFileChooser();
		fileChooser.setDialogTitle("Abrir arquivo XML...");
		fileChooser.setFileFilter(new FileNameExtensionFilter("Arquivos XML",
				"xml"));
		fileChooser.setVisible(true);

		int tmp = fileChooser.showOpenDialog(this);

		if (tmp != JFileChooser.APPROVE_OPTION) {
			return;
		}

		File file = fileChooser.getSelectedFile();

		this.painelGrafico.limparTela();
		xml.abrirXML(file.getAbsolutePath());

		// Carrega as informacoes de conexao do XML
		xml.getCabecalhoConfiguracao();

		if (DBUtil.getInstance().isCanConnect()) {
			executarFuncaoControle(0);
		}

		List<List<?>> elementos = xml.getModeloGrafico();

		for (int i = 0; i < elementos.get(0).size(); i++) {

			if (elementos.get(0).get(i) instanceof GraficoTabela) {
				this.painelGrafico.inserirTabelaNoGrafico(
						((GraficoTabela) elementos.get(0).get(i)).toString(),
						((GraficoTabela) elementos.get(0).get(i)).getX(),
						((GraficoTabela) elementos.get(0).get(i)).getY());
				this.painelGrafico.limparAtributo();
			}

			if (elementos.get(0).get(i) instanceof GraficoOperador) {

				int tipo = Operadores
						.getOperadorType((GraficoOperador) elementos.get(0)
								.get(i));
				this.painelGrafico.setNovoOperador(tipo);

				String parametro1 = "";
				String parametro2 = "";

				if (((GraficoOperador) elementos.get(0).get(i)).toString()
						.equals(Operadores.SELECAO.getOperador())
						|| ((GraficoOperador) elementos.get(0).get(i))
								.toString().equals(
										Operadores.PROJECAO.getOperador())
						|| ((GraficoOperador) elementos.get(0).get(i))
								.toString().equals(
										Operadores.ORDENACAO.getOperador())
						|| ((GraficoOperador) elementos.get(0).get(i))
								.toString().equals(
										Operadores.JUNCAO_EX_ESQUERDA
												.getOperador())) {
					parametro1 = ((GraficoOperador) elementos.get(0).get(i))
							.getParametro(1);
				}

				if (((GraficoOperador) elementos.get(0).get(i)).toString()
						.equals(Operadores.AGRUPAMENTO.getOperador())) {
					parametro1 = ((GraficoOperador) elementos.get(0).get(i))
							.getParametro(1);
					parametro2 = ((GraficoOperador) elementos.get(0).get(i))
							.getParametro(2);
				}

				this.painelGrafico.inserirOperadorNoGrafico(parametro1,
						parametro2, ((GraficoOperador) elementos.get(0).get(i))
								.getX(), ((GraficoOperador) elementos.get(0)
								.get(i)).getY());
				this.painelGrafico.limparAtributo();
			}
		}

		painelGrafico.refresh();

		for (int i = 0; i < elementos.get(1).size(); i++) {
			if (elementos.get(1).get(i) instanceof GraficoLigacao) {
				this.painelGrafico.inserirLigacaoNoGrafico(
						((GraficoLigacao) elementos.get(1).get(i))
								.getPosicaoPai().x, ((GraficoLigacao) elementos
								.get(1).get(i)).getPosicaoPai().y,
						((GraficoLigacao) elementos.get(1).get(i))
								.getPosicaoFilho().x,
						((GraficoLigacao) elementos.get(1).get(i))
								.getPosicaoFilho().y);
				this.painelGrafico.limparAtributo();
			}
		}

		painelGrafico.limparAtributo();
		arquivoAberto = true;

	}// GEN-LAST:event_btAbrirMousePressed

